package JavaBasics;

public class StringConcate {

	public static void main(String[] args) {
		int a=100;
		int b=100;
		
		String x="Hello";
		String y="World";
		
		System.out.println(a+b);
		System.out.println(y=x);
		System.out.println(x+y+(a+b));
		System.out.println(a+b+x+y);
		System.out.println(x);
		
	}

}
